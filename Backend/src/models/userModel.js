const mongoose = require('mongoose');

const userSchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true,
    },
    lastName: {
        type: String,
        required: true,
    },
    instituteName: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ['admin', 'instructor', 'student'],
        default: 'instructor', // Adjusted for instructor registration
    }
},
{
    timestamps: true,
});

const User = mongoose.model('User', userSchema);
module.exports = User;
